############################################################
# Generate the greatest common divisor.
############################################################
def extended_gcd(a, b):
    if b == 0:
        return (1, 0)
    else:
        q = a / int(b)
        r = a % b

        s, t = extended_gcd(b, r)
        return (t, s - q * t)

############################################################
# Return the GCD
############################################################
def gcd(a,b):
	t, ans = extended_gcd(a,b)
	return ans if ans > 0 else ans + a

